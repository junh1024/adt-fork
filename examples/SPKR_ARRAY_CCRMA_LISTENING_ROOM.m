function [ val ] = SPKR_ARRAY_CCRMA_LISTENING_ROOM(c)
    %ARRAY_CCRMA_LISTENING_ROOM Speaker positions
    %   Detailed explanation goes here
    
    if ~exist('c', 'var')
        c = 'full';
    end
    
    val.id = {'H1', 'H2', 'H3', 'H4', 'H5', 'H6', 'H7', 'H8', ...
        'T1', 'T2', 'T3', 'T4', 'T5', 'T6', ...
        'B1', 'B2', 'B3', 'B4', 'B5', 'B6', ...
        'TT', 'BB'};
    
    % azimuth (deg), elevation (deg), distance (meters)
    switch lower(c)
        case 'full'
            val.name = 'CCRMA_Listening_Room';
            % as built, extracted from ambdec config file supplied by 'Nando
            S = [
                  22.5000         0    3.1200
                  67.5000         0    3.1200
                 112.5000         0    3.1200
                 157.5000         0    3.1200
                -157.5000         0    3.1200
                -112.5000         0    3.1200
                 -67.5000         0    3.1200
                 -22.5000         0    3.1200
                  30.0000   40.0000    2.1200
                  90.0000   40.0000    2.2300
                 150.0000   40.0000    2.1100
                -150.0000   40.0000    2.1100
                 -90.0000   40.0000    2.2300
                 -30.0000   40.0000    2.1200
                  30.0000  -50.0000    2.3300
                  90.0000  -50.0000    2.2800
                 150.0000  -50.0000    2.2200
                -150.0000  -50.0000    2.2200
                 -90.0000  -50.0000    2.2800
                 -30.0000  -50.0000    2.3300
                   0        90.0000    1.4400
                   0       -90.0000    1.9000
                ];
     case 'full45'
         val.name = 'CCRMA_Listening_Room_45deg';
         % regularize up an down rings to 45 deg
            S = [
                  22.5000         0    3.1200
                  67.5000         0    3.1200
                 112.5000         0    3.1200
                 157.5000         0    3.1200
                -157.5000         0    3.1200
                -112.5000         0    3.1200
                 -67.5000         0    3.1200
                 -22.5000         0    3.1200
                  30.0000   45.0000    2.1200
                  90.0000   45.0000    2.2300
                 150.0000   45.0000    2.1100
                -150.0000   45.0000    2.1100
                 -90.0000   45.0000    2.2300
                 -30.0000   45.0000    2.1200
                  30.0000  -45.0000    2.3300
                  90.0000  -45.0000    2.2800
                 150.0000  -45.0000    2.2200
                -150.0000  -45.0000    2.2200
                 -90.0000  -45.0000    2.2800
                 -30.0000  -45.0000    2.3300
                   0        90.0000    1.4400
                   0       -90.0000    1.9000
                ];
        case 'horizontal'
            val.name = 'CCRMA_Listening_Room_8-speaker-horizontal';
            % horizontal ring
             S = [
                22.5000         0    3.1200
                67.5000         0    3.1200
                112.5000         0    3.1200
                157.5000         0    3.1200
                -157.5000         0    3.1200
                -112.5000         0    3.1200
                -67.5000         0    3.1200
                -22.5000         0    3.1200
                ];
            val.id = {'H1', 'H2', 'H3', 'H4', 'H5', 'H6', 'H7', 'H8'};
            
        case 'dome'
            val.name = 'CCRMA_Listening_Room_Dome';
            S = [
                  22.5000         0    3.1200
                  67.5000         0    3.1200
                 112.5000         0    3.1200
                 157.5000         0    3.1200
                -157.5000         0    3.1200
                -112.5000         0    3.1200
                 -67.5000         0    3.1200
                 -22.5000         0    3.1200
                  30.0000   40.0000    2.1200
                  90.0000   40.0000    2.2300
                 150.0000   40.0000    2.1100
                -150.0000   40.0000    2.1100
                 -90.0000   40.0000    2.2300
                 -30.0000   40.0000    2.1200
                   0        90.0000    1.4400
                ];
            val.id = {'H1', 'H2', 'H3', 'H4', 'H5', 'H6', 'H7', 'H8', ...
                'T1', 'T2', 'T3', 'T4', 'T5', 'T6', ...
                'TT'};
        otherwise
            error('unknown variant c = %s', c);
    end
    val.az = S(:,1)*pi/180;
    val.el = S(:,2)*pi/180;
    val.r = S(:,3);
    
    % direction cosines, unit vector
    [val.x, val.y, val.z] = sph2cart(val.az, val.el, 1);
    
    fprintf('%s\n', val.name);
end
